# ตรวจสอบว่ามีอาร์กิวเมนต์ถูกส่งเข้ามาหรือไม่
if [ $# -eq 0 ]; then
    echo "No arguments supplied"
else
    # แสดงผลเฉพาะ 3 อาร์กิวเมนต์แรกเท่านั้น
    for arg in "$1" "$2" "$3"
    do
        # ตรวจสอบว่าตัวแปรมีค่า (เพื่อหลีกเลี่ยงการพิมพ์ค่าที่ว่างเปล่า)
        if [ -n "$arg" ]; then
            echo "$arg"
        fi
    done
fi
